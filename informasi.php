    <h1>Informasi Polycystic Ovarian Syndrome (PCOS)</h1>
  </div>
  <div>
    <div>
      <p><strong>Sindrom polikistik ovarium atau <em>polycystic ovarian syndrome</em> (PCOS) adalah gangguan hormon yang terjadi pada wanita di usia subur. Penderita PCOS mengalami gangguan menstruasi dan </strong><strong>memiliki </strong><strong>kadar hormon maskulin (hormon androgen) yang berlebihan.</strong></p>
      <p>Hormon androgen yang berlebih pada penderita PCOS dapat mengakibatkan ovarium atau indung telur memproduksi banyak kantong-kantong berisi cairan. Akibatnya, sel telur tidak berkembang sempurna dan gagal dilepaskan secara teratur.</p>
      <img src="https://res.cloudinary.com/dk0z4ums3/image/upload/v1616372569/attached_image/pcos-0-alodokter.jpg" alt="PCOS - alodokter" width="650" height="433">
      <p>Akibat dari polycystic ovarian syndrome juga dapat menyebabkan <a href="https://www.alodokter.com/kista-hambatan-wanita-memiliki-keturunan" target="_blank">penderitanya tidak subur (mandul)</a>, serta lebih rentan terkena diabetes dan tekanan darah tinggi.</p>
      <h3><strong>Gejala Polycystic Ovarian Syndrome (PCOS)</strong></h3>
      <p>Gejala sindrom ovarium polikistik bisa timbul ketika seorang wanita mengalami haid pertama kali saat masa pubertas. Meski gejala <a href="https://www.alodokter.com/pcos-pada-remaja-serta-pengobatannya" target="_blank">PCOS sering muncul saat remaja</a>, ada juga penderita PCOS yang baru mengalami gejalanya setelah dewasa atau saat periode tertentu, misalnya ketika mengalami kenaikan berat badan secara signifikan. Berikut adalah gejala PCOS:</p>
      <ul>
        <li><strong>Gangguan menstruasi<br>
        </strong>PCOS kerap ditandai dengan periode <a href="https://www.alodokter.com/menstruasi" target="_blank">menstruasi</a> yang tidak teratur atau berkepanjangan. Sebagai contoh, penderita PCOS hanya akan mengalami haid kurang dari 8-9 kali dalam setahun. Jarak antar haid dapat kurang dari 21 hari atau lebih dari 35 hari, atau darah menstruasi mengalir deras.</li>
        <li><strong>Gejala akibat kadar hormon androgen yang meningkat<br>
        </strong>Peningkatan kadar <a href="https://www.alodokter.com/mengenal-fungsi-penting-hormon-androgen-pada-pria-dan-wanita" target="_blank">hormon androgen</a> pada wanita dengan PCOS dapat menyebabkan munculnya gejala fisik seperti pria, seperti tumbuhnya rambut yang lebat di wajah dan tubuh (<a href="https://www.alodokter.com/hirsutisme" target="_blank">hirsutisme</a>), serta munculnya <a href="https://www.alodokter.com/jerawat" target="_blank">jerawat</a> yang parah dan <a href="https://www.alodokter.com/botak" target="_blank">kebotakan</a>.</li>
        <li><strong>Menderita kista ovarium yang banyak<br>
        </strong>Pada penderita PCOS, bisa ditemukan kantong-kantong kista di sekitar sel telur (ovarium).</li>
        <li><strong>Warna kulit menjadi gelap<br>
        </strong>Beberapa bagian tubuh penderita PCOS bisa menjadi gelap, terutama di daerah lipatan, yaitu lipatan leher, selangkangan, dan bagian bawah payudara.</li>
      </ul>
      <h4><strong>Kapan harus ke dokter</strong></h4>
      <p>Periksakan diri ke dokter jika muncul gejala PCOS, seperti haid yang tidak teratur. Polycystic ovarian syndrome yang tidak ditangani bisa mengakibatkan penderitanya sulit untuk hamil atau <a href="https://www.alodokter.com/mandul-atau-tidak-pastikan-dengan-tes-kesuburan" target="_blank">mandul</a> karena sel telur tidak dapat dilepaskan (tidak ada ovulasi).</p>
      <p>Penderita PCOS yang sedang hamil juga berisiko <a href="https://www.alodokter.com/kelahiran-prematur" target="_blank">melahirkan bayi secara prematur</a>, mengalami <a href="https://www.alodokter.com/keguguran" target="_blank">keguguran</a>, menderita tekanan darah tinggi, dan mengalami <a href="https://www.alodokter.com/diabetes-gestasional" target="_blank">diabetes gestasional</a>. Oleh karena itu, lakukan kontrol rutin ke <a href="https://www.alodokter.com/cari-dokter/dokter-kandungan" target="_blank">dokter kandungan</a> selama hamil agar kondisi kesehatan ibu dan janin terpantau.</p>
      <h3><strong>Penyebab Polycystic Ovarian Syndrome (PCOS)</strong></h3>
      <p>Sampai saat ini, belum diketahui dengan pasti apa yang menyebabkan PCOS. Namun, ada beberapa faktor yang diduga sebagai penyebab PCOS, yaitu:</p>
      <ul>
        <li><strong>Kelebihan hormon insulin<br>
        </strong><a href="https://www.alodokter.com/pentingnya-hormon-insulin-dalam-mengendalikan-gula-darah" target="_blank">Hormon insulin</a> adalah hormon yang menurunkan kadar gula dalam darah. Insulin yang berlebih akan membuat tubuh meningkatkan produksi hormon androgen dan mengurangi sensitivitas tubuh terhadap insulin.</li>
        <li><strong>Faktor genetik<br>
        </strong>Hal ini karena sebagian penderita PCOS juga memiliki anggota keluarga yang menderita PCOS.</li>
      </ul>
      <h3><strong>Diagnosis</strong> <strong>Polycystic Ovarian Syndrome (PCOS)</strong></h3>
      <p>Tidak ada tes yang dapat dilakukan untuk langsung mendiagnosis PCOS. Oleh karena itu, biasanya dokter akan menanyakan ada tidaknya gejala polycystic ovarian syndrome pada penderita. Selain itu, dokter juga akan melakukan pemeriksaan fisik untuk menemukan tanda-tanda dari penyakit ini.</p>
      <p>Pemeriksaan fisik dilakukan untuk melihat adanya pertumbuhan rambut berlebih atau adanya jerawat yang parah. Pemeriksaan fisik ini juga termasuk pemeriksaan dalam untuk memeriksa <a href="https://www.alodokter.com/mengenal-organ-reproduksi-wanita" target="_blank">organ reproduksi wanita</a>.</p>
      <p>Setelah pemeriksaan fisik dilakukan, dokter akan melakukan pemeriksaan penunjang yang meliputi:</p>
      <ul>
        <li>Tes darah, untuk memeriksa kadar hormon androgen, tes toleransi terhadap gula darah, dan <a href="https://www.alodokter.com/cari-rumah-sakit/laboratorium-klinik/cek-kolesterol" target="_blank">kadar kolestrol</a> yang sering kali meningkat pada penderita PCOS.</li>
        <li><a href="https://www.alodokter.com/cari-rumah-sakit/radiologi/usg-panggul" target="_blank">USG panggul</a>, untuk memeriksa ketebalan lapisan rahim pasien dengan bantuan gelombang suara.</li>
      </ul>
      <p>Jika penderita sudah dipastikan menderita PCOS, maka dokter akan melakukan sejumlah tes lain untuk mendeteksi komplikasi yang mungkin terjadi akibat PCOS.</p>
      <h3><strong>Pengobatan </strong><strong>Polycystic Ovarian Syndrome (PCOS)</strong></h3>
      <p>Pengobatan bagi tiap penderita PCOS berbeda-beda, tergantung pada gejala yang dialaminya, seperti kemandulan, hirsutisme, atau jerawat parah. Secara umum, PCOS dapat ditangani dengan beberapa cara berikut ini:</p>
      <h4><strong>Perubahan gaya hidup</strong></h4>
      <p>Dokter akan merekomendasikan olahraga dan diet rendah kalori untuk <a href="https://www.alodokter.com/7-cara-mudah-menurunkan-berat-badan" target="_blank">menurunkan berat badan</a>. Hal ini karena gejala sindrom ovarium polikistik akan mereda seiring penurunan berat badan penderita. Olahraga juga berguna untuk meningkatkan efektivitas obat dan membantu meningkatkan kesuburan penderita PCOS.</p>
      <h4><strong>Obat-obatan</strong></h4>
      <p>Dokter dapat memberikan kombinasi <a href="https://www.alodokter.com/pil-kb" target="_blank">pil KB</a> dengan obat lain untuk mengontrol siklus menstruasi. Hormon estrogen dan progesteron dalam pil KB dapat menekan produksi hormon androgen dalam tubuh.</p>
      <p>Dokter juga dapat merekomendasikan konsumsi hormon <a href="https://www.alodokter.com/progesteron" target="_blank">progesteron</a> saja selama 10-14 hari selama 1-2 bulan. Penggunaan hormon ini dapat mengatur siklus haid yang terganggu.</p>
      <p>Obat-obatan lain yang dapat digunakan untuk menormalkan kembali siklus haid dan membantu ovulasi adalah:</p>
      <ul>
        <li><a href="https://www.alodokter.com/clomifene" target="_blank">Clomifene</a></li>
        <li>Letrozole</li>
        <li><a href="https://www.alodokter.com/metformin" target="_blank">Metformin</a></li>
      </ul>
      <p>Selain pil KB, untuk mengurangi gejala hirsutisme akibat hormon androgen yang berlebih, dokter dapat memberikan obat <a href="https://www.alodokter.com/spironolactone" target="_blank">spironolactone</a>. Spironolactone dapat menangkal efek androgen pada kulit, yaitu tumbuhya rambut yang lebat dan jerawat yang parah.</p>
      <h4><strong>Prosedur medis khusus</strong></h4>
      <p>Selain beberapa metode pengobatan di atas, dokter dapat menganjurkan pasien untuk melakukan <em>electrolysis</em> untuk menghilangkan rambut di tubuh. Dengan aliran listrik rendah, <em>electrolysis</em> akan menghancurkan folikel rambut dalam beberapa kali terapi.</p>
      <h3><strong>Komplikasi </strong><strong>Polycystic Ovarian Syndrome (PCOS)</strong></h3>
      <p>PCOS yang tidak ditangani dapat membuat penderitanya berisiko mengalami  komplikasi berikut ini:</p>
      <ul>
        <li>Gangguan tidur</li>
        <li>Gangguan makan</li>
        <li>Gangguan kecemasan dan <a href="https://www.alodokter.com/depresi" target="_blank">depresi</a></li>
        <li>Kemandulan</li>
        <li>Keguguran atau kelahiran bayi prematur</li>
        <li>Hipertensi saat hamil</li>
        <li>Diabetes dan diabetes gestasional</li>
        <li><a href="https://www.alodokter.com/hepatitis" target="_blank">Hepatitis</a></li>
        <li>Sindrom metabolik</li>
        <li><a href="https://www.alodokter.com/kanker-endometrium" target="_blank">Kanker endometrium</a></li>
      </ul>
      <h3><strong>Pencegahan </strong><strong>Polycystic Ovarian Syndrome (PCOS)</strong></h3>
      <p>PCOS sulit dicegah, tetapi dengan menjaga berat badan ideal, gejala dan risiko komplikasinya dapat dikurangi. Berikut adalah cara yang bisa dilakukan untuk menjaga berat badan ideal:</p>
      <ul>
        <li>Batasi konsumsi makanan manis</li>
        <li>Perbanyak konsumsi serat</li>
        <li>Olahraga secara teratur</li>
      </ul>
